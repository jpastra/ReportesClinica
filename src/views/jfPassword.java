    public void validar() {
        rs = Conexiondb.Consulta("SELECT USUCVE, USUPAS FROM USUCAT WHERE USUCVE='" + jt_user.getText() + "' AND USUPAS='" + jp_pass.getText() + "'");
        try {
            //ResultSetMetaData rsmt = rs.getMetaData();

            if (rs.next()) {

                String user = rs.getString(1).toString().trim();
                String pass = rs.getString(2).toString().trim();

                String usertxt = jt_user.getText().toUpperCase();
                String passtxt = jp_pass.getText().trim();

                if (user.equals(usertxt) && pass.equals(passtxt)) {
                    jt_user.setText(null);
                    jp_pass.setText(null);

                    hideFrame();
                    MenuPrincipal menuprinc = new MenuPrincipal();
                    menuprinc.setVisible(true);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Usuario o Password invalido", "Error de Credenciales", JOptionPane.ERROR_MESSAGE);
                jt_user.setText(null);
                jp_pass.setText(null);
                jt_user.grabFocus();
            }

        } catch (SQLException ex) {
            Logger.getLogger(jfPassword.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

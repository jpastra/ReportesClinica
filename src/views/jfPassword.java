    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        if (jt_user.getText().isEmpty() && jp_pass.getText().length() > 0) {
            JOptionPane.showMessageDialog(null, "Ingresa el Usuario", "Error de Credenciales", JOptionPane.ERROR_MESSAGE);
            jt_user.setText(null);
            jp_pass.setText(null);
        } else if (jt_user.getText().isEmpty() && jp_pass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingresa el Usuario y Password", "Error de Credenciales", JOptionPane.ERROR_MESSAGE);

        } else if (jt_user.getText().length() > 0 && jp_pass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingresa el Password", "Error de Credenciales", JOptionPane.ERROR_MESSAGE);

        } else if (jt_user.getText().length() > 0 && jp_pass.getText().length() > 0) {
            task01Inf = new PBvalida();
            task01Inf.execute();
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

